public class operatorSwichCase {
    public static void main(String[] args) {
        int num = 451;
        switch (num){
            case 12:
                System.out.print("Num is 12");
                break;
            case 545:
                System.out.print("Num is 545");
                break;
            case 23:
                System.out.print("Num is 23");
            default:
                System.out.print("Num is unknown");

        }
    }
}
